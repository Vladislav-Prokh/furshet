function renderGallery() {
  if (!galleryGrid) return;

  galleryImages.forEach((imageName, index) => {
    const card = document.createElement("figure");
    card.className = "gallery-card";

    const image = document.createElement("img");
    image.src = `./${imageName}`;
    image.alt = `Фуршетная работа ${index + 1}`;
    image.loading = "lazy";

    card.appendChild(image);
    galleryGrid.appendChild(card);
  });
}

function renderReviews(lang) {
  if (!reviewsGrid) return;
  const reviews = reviewsByLang[lang] || reviewsByLang.ru;
  reviewsGrid.innerHTML = "";

  reviews.forEach((review) => {
    const card = document.createElement("article");
    card.className = "review-card";

    const head = document.createElement("div");
    head.className = "review-card-head";

    const name = document.createElement("strong");
    name.className = "review-card-name";
    name.textContent = review.name;

    const date = document.createElement("span");
    date.className = "review-card-date";
    date.textContent = review.date;

    const stars = document.createElement("div");
    stars.className = "review-card-stars";
    stars.textContent = "★★★★★";

    const text = document.createElement("p");
    text.className = "review-card-text";
    text.textContent = review.text;

    head.appendChild(name);
    head.appendChild(date);
    card.appendChild(head);
    card.appendChild(stars);
    card.appendChild(text);
    reviewsGrid.appendChild(card);
  });
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.ru;
  const seo = seoByLang[lang] || seoByLang.ru;

  i18nNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (!key || !dict[key]) return;
    node.textContent = dict[key];
  });

  document.documentElement.lang = lang === "uk" ? "uk" : "ru";
  document.title = seo.title;
  if (metaDescription) metaDescription.setAttribute("content", seo.description);
  if (ogTitle) ogTitle.setAttribute("content", seo.ogTitle);
  if (ogDescription) ogDescription.setAttribute("content", seo.ogDescription);
  if (ogLocale) ogLocale.setAttribute("content", seo.locale);
  if (twitterTitle) twitterTitle.setAttribute("content", seo.ogTitle);
  if (twitterDescription) twitterDescription.setAttribute("content", seo.ogDescription);

  if (structuredData) {
    structuredData.textContent = JSON.stringify(
      {
        "@context": "https://schema.org",
        "@type": "FoodEstablishment",
        name: "Furshet SV",
        url: "https://furshets.com/",
        image: "https://furshets.com/3905034972557592392.jpg",
        description: seo.schemaDescription,
        telephone: "+380684999394",
        sameAs: ["https://www.instagram.com/furshet.sv/"],
        servesCuisine: ["Finger food", "Buffet catering", "Party snacks"],
        areaServed: {
          "@type": "City",
          name: lang === "uk" ? "Запоріжжя" : "Запорожье"
        },
        address: {
          "@type": "PostalAddress",
          addressLocality: lang === "uk" ? "Запоріжжя" : "Запорожье",
          addressCountry: "UA"
        }
      },
      null,
      2
    );
  }

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langBtn === lang);
  });

  renderReviews(lang);
  localStorage.setItem("furshet-lang", lang);
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langBtn);
  });
});

renderGallery();
